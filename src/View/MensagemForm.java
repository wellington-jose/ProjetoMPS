    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // capturar as mensagens e os bot√µes
        String mensagem = jTextFieldMensagem.getText();

        if (jCheckBoxEmail.isSelected()) {
            control.enviarMensagemEmail(mensagem, "roberto@gamail.com");
        }

        if (jCheckBoxSMS.isSelected()) {
            control.enviarMensagemSMS(mensagem, "987679231");
        }

        if (jCheckBoxWhatsapp.isSelected()) {
            control.enviarMensagemWhatsApp(mensagem, "987679231");
        }

        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

